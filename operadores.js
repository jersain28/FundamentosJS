// Operadores Matematicos
// + - * / % **
// += -= *= /= %= **=
console.log(10 + 10);
console.log(10 - 10);
console.log(10 / 10);
console.log(10 * 10);
console.log(10 % 10);
console.log(10 ** 10); // Potencia

let num= 15;
console.log(num += 5); // num = num + 5
console.log(num -= 5);
console.log(num *= 5);
console.log(num /= 5);
console.log(num %= 5);
console.log(num **= 5);

// Operadores Booleanos 
// > < >= <= !== ===
console.log(1 > 2);
console.log(1 < 2);
console.log(1 >= 2);
console.log(1 <= 2);
console.log(1 !== 2);
console.log(1 === 2);

// Operadores Logicos
// and &&
console.log(true && true);
// or ||
console.log( true || true);
// not !
console.log(!true);